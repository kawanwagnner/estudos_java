package Desafios_27_01;

public class ExemploArray {

	public static void main(String[] args) {

		int numeros[] = new int[5];
		numeros[0] = 12;
		numeros[1] = 19;
		numeros[2] = 5;
		numeros[3] = 1;
		numeros[4] = 321;

		for (int i = 0; i < 5; i++) {
			System.out.println(numeros[i]);
		}

	}

}
